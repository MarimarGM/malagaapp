package com.turismo.malagapp.model.weather;

public class CoordResponseDto {

    private String lon;
    private String lat;

    public CoordResponseDto() {
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }
}
